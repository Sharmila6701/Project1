package pattern;

public class pattern1 {
	public static void main(String[] args){
        int rows = 5; // You can change this value to control the number of rows.

        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }


}
